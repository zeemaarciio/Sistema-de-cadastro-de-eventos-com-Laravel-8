     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $search = request('search');

        if($search) {
            
            $events = Event::where([
                ['title', 'like', '%'. $search . '%']
            ])->get();
            
        } else {
            $events = Event::all();
        }
        
        return view('welcome', ['events' => $events, 'search' => $search]);
    }

